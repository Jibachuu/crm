export default function HelpClient() {
  const [search, setSearch] = useState("");
  const [openId, setOpenId] = useState<string | null>(null);

  const filtered = ARTICLES.filter((a) => {
    if (!search) return true;
    const q = search.toLowerCase();
    return a.title.toLowerCase().includes(q) || a.tags.some((t) => t.includes(q)) || a.content.toLowerCase().includes(q);
  });

  return (
    <div className="max-w-3xl mx-auto">
      {/* Search */}
      <div className="relative mb-6">
        <Search size={18} className="absolute left-3 top-1/2 -translate-y-1/2" style={{ color: "#aaa" }} />
        <input value={search} onChange={(e) => setSearch(e.target.value)}
          placeholder="Поиск по справке... (например: счёт, КП, пробники, скидка)"
          className="w-full pl-10 pr-4 py-3 text-sm rounded-lg focus:outline-none focus:ring-2 focus:ring-blue-500"
          style={{ border: "1px solid #d0d0d0" }} />
      </div>

      {/* Articles */}
      <div className="space-y-2">
        {filtered.map((article) => {
          const isOpen = openId === article.id;
          return (
            <div key={article.id} className="rounded-lg overflow-hidden" style={{ border: "1px solid #e4e4e4", background: "#fff" }}>
              <button onClick={() => setOpenId(isOpen ? null : article.id)}
                className="w-full flex items-center gap-3 px-4 py-3 text-left hover:bg-gray-50 transition-colors">
                <span className="text-xl">{article.icon}</span>
                <span className="flex-1 text-sm font-medium" style={{ color: "#333" }}>{article.title}</span>
                {isOpen ? <ChevronDown size={16} style={{ color: "#888" }} /> : <ChevronRight size={16} style={{ color: "#888" }} />}
              </button>
              {isOpen && (
                <div className="px-4 pb-4 pt-0 text-sm whitespace-pre-wrap" style={{ color: "#555", lineHeight: 1.7, borderTop: "1px solid #f0f0f0" }}>
                  <div className="pt-3">
                    {article.content.split("\n").map((line, i) => {
                      if (line.startsWith("**") && line.endsWith("**")) return <h3 key={i} className="font-semibold mt-2 mb-1" style={{ color: "#333" }}>{line.replace(/\*\*/g, "")}</h3>;
                      if (line.startsWith("**")) return <p key={i} className="font-semibold mt-2" style={{ color: "#333" }}>{line.replace(/\*\*/g, "")}</p>;
                      if (line.startsWith("•")) return <p key={i} className="pl-4">{line}</p>;
                      return <p key={i}>{line}</p>;
                    })}
                  </div>
                </div>
              )}
            </div>
          );
        })}
      </div>

      {filtered.length === 0 && (
        <div className="text-center py-12" style={{ color: "#aaa" }}>
          <Search size={36} className="mx-auto mb-2" style={{ color: "#ddd" }} />
          <p className="text-sm">Ничего не найдено по запросу "{search}"</p>
        </div>
      )}
    </div>
  );
}
